nombres = ["juan", "Carlos", "valentina", "Maria josé"]

# Ordenar la lista alfabéticamente (ascendente)
nombres.sort()

# Ordenar la lista alfabéticamente en orden descendente
nombres.sort(reverse=True)

#ordenar la lista alfabéticamente en orden ascendiente
nombres.sort(True)

# Revertir el orden de la lista
nombres.reverse()

print(nombres)